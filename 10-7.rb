# What's the major difference between an Array and a Hash?
 
# A hash stores its values in an index location => value structure, and is therefore referenced by either the value at a sequential location, or a sequential location at a value. A hash stores its values in a key => value order, and is therefore referenced by the value at key, or the key at value.
 
=begin 
The major difference between an array and a hash is that a hash contains a key value pair for referencing by key.
=end